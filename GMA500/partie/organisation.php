				<h1> Programme de la soirée </h1>
				
				<p> 
					 Cette soirée est l'occasion pour le GMA 500 de vous inviter à partager un moment de convivialité et de découverte 
					 autour de projections de 3 films de montagne et d'échanges avec leurs réalisateurs. <br><br>

					Pour ne rien changer, nous avons tout prévu pour que vous passiez une bonne soirée : un coin bar, de quoi 
					vous restaurer avec les encas salés et sucrés de l'Auberge de Gourdon, des lots et bien sur trois films. <br><br>
					
					Il est possible de réserver vos boissons et encas sucrés et salés lors de votre inscription (ne vous fiez
					pas à la mention "épuisé", l'option est disponible en page 2).
					Les premiers inscrits recevront des lots de nos partenaires. <br><br>

					Frédi Meignan, le gardien du refuge du Promontoir et Lionel Daudet, que l'on ne présente plus, seront présents 
					pour répondre à vos questions. Et le colibri, Lucas Duperrex sera aussi parmis nous pour vous parler de son 
					aventure en Amérique du Sud. 
			
