						<h4> Big Walls Big Seas </h4>
						
							<p> Après la Géorgie du Sud en 2008 et l’Antarctique en 2010, Isabelle Autissier et Lionel Daudet repartent ensemble pour une nouvelle aventure au Groenland. Une mission sportive, environnementale et scientifique, menée en partenariat avec SNEF, qui débutera le 10 juin pour s’achever à la mi-septembre. C’est à bord de l’ADA II, un voilier doté d’une coque d’aluminium capable de naviguer dans les glaces Arctiques, que les deux explorateurs se rendront au sud-ouest du Groenland, avant de se séparer. Tous deux seront reliés par téléphone satellite. <br><br>
								Isabelle Autissier, à la voile, naviguera aux abords du site de Torssukatak, un lieu isolé où rares sont les explorateurs à s’y être aventurés. Lionel Daudet, alpiniste aventurier, partira quant à lui à la découverte de cette terre d’exploration, avec ses murs lisses et vertigineux : les « big walls ». Il s’attaquera à la plus grande parois, d’une hauteur de 1 600 mètres ! Une aventure entre mer et montagnes, équipage et cordée, en collaboration avec le CNRS, au cours de laquelle Lionel Daudet et Isabelle Autissier effectueront des prélèvements de flore et de plancton au sein d’un écosystème encore peu exploré. 
							</p>	
							
