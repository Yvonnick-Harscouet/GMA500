				<h1> Les intervenants </h1>
				
				
					<img src="image/dupperex.jpg"/>
				
					<h3>
						Lucas DUPERREX, originaire de la vallée de Chamonix, il est étudiant ingénieur à Grenoble et athlète de haut niveau. En 2015 il a remporté son premier titre international sur le relais 4x2.5km des championnats du monde handisport en tant que guide d’Anthony Chalençon, skieur non voyant. 
					</h3>
				
						<div class="clear"></div>
				
					<img src="image/meignan.jpg"/>
				
					<h3>
						Fredi MEIGNAN, gardien du refuge du promontoire et président de Mountain Wilderness France, était un parisien amoureux et passionné de la montagne. Il a décidé de tout quitter pour devenir le gardien du refuge du Promontoire, perché à 3092m d’altitude. Il décrit le refuge comme un élément important, non essentiel, mais qui compte beaucoup dans le souvenir, le vécu que l’on a d’une course. Comme tous les gardiens de refuge, il a dans ses tiroirs quelques belles anecdotes... 				</h3>
					</h3>
					
						<div class="clear"></div>
					
					<img src="image/daudet.jpg"/>
				
					<h3>
						Lionnel DAUDET, dit le Dod, est un alpiniste, écrivain et photographe français né à Saumur. Après avoir réalisé de nombreuses première aux quatre coin de la planète, il a mis de côté les exploits techniques pour appréhender les grands espaces autrement. C’est en réfléchissant à la notion de frontière qu’il boucle en 2012, un « exact tour de France ». En 2016, il embarque, au-delà de toutes les frontières, avec la navigatrice Isabelle Autissier… 					</h3>
					</h3>
					
