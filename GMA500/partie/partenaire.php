				<h1> Qui sommes nous ? </h1>
				
				<p> Nous sommes le Groupe Montagne Altitude 500 (GMA500), une association de montagne et d'escalade du pays de Grasse, affiliée à la FFME
					et comptant 200 membres. Nous pratiquons toute l'année l'escalade, l'alpinisme, le ski de rando, les raquettes, le canyoning et le VTT. 
				</p>
				
				<h1> La soirée du film de montagne du GMA 500 : </h1>
				
				<p> Elle est organisée chaque année avec la projection de 2 ou 3 films suivis d'un temps d'échanges avec leur rélisateur ou protagonistes. 
					C'est une soirée de découverte et de rencontre autour de la montagne et des activités de pleine nature qui réunit chaque année 300 participants.
					Cette année nous vous accueillerons à l’Espace Culturel et Sportif du Val de Siagne. L'affiche diffusée dans tout le département plus une promotion
					sur internet et dans les médias locaux (presse papier et radio Agora FM). 
				</p>
				
				<h1> Ce que nous pouvons vous apporter : </h1>
				
				<ul>
