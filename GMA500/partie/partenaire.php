					<li> Votre logo et lien sur la page internet dédiée à  la soirée et sur le site internet de la FFME (1000 vues pour l'édition 2015) </li>
					<li> De la publicité dans la salle lors de la soirée (banderole, affiches, flyers, etc.) </li>
					<li> La possibilité de tenir un stand lors de la soirée pour présenter et vendre vos produits </li>
